                18</p>

        </div>

        <div class="col-md-4">
            <h4 Align="center">PREPARACIÓN</h4>
            <p Align="justify">1 Precaliente el horno a 190 °C. Engrase con margarina un molde para brazo de gitano de 30 x 23 cm y fórrelo con papel vegetal. Tamice en un cuenco la harina, el cacao y la levadura. Añada el azúcar, la margarina y el huevo. Bátalo hasta que esté homogéneo y vierta la pasta en el molde. Cueza el bizcocho 15 o 20 minutos o hasta que haya subido y esté consistente al tacto. Déjelo en el molde 5 minutos y después páselo a una rejilla metálica para que se enfríe.</p>
            <p Align="justify">2 Recorte un redondel de bizcocho utilizando como guía el borde de un molde cóncavo de 1,2 litros de capacidad; resérvelo. Forre el molde con film transparente. Con el resto del bizcocho, forre el molde. Mezcle el brandy y el kirsch en un bol pequeño. Rocíe el bizcocho con el licor (también el redondel).</p>
            <p Align="justify">3 Para hacer el relleno, ponga la nata en un cuenco aparte y eche el azúcar de lustre. Bata hasta que se espese y añada el picadillo de almendra, las cerezas y el chocolate. Rellene el bizcocho con la mezcla y tápelo con el redondel, presionando bien. Cubra el pastel con un plato y ponga un peso encima. Déjelo enfriar en el frigorífico de 6 a 8 horas, o toda la noche. Cuando vaya a servirlo, vuelque el zucotto en una fuente y adórnelo con cacao en polvo y azúcar de lustre, tamizándolos por encima, y unas cerezas frescas.</p>
