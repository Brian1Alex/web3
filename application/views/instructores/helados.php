        <h2 Align="center">CASSATA SICILIANA</h2>

        <div class="col-md-4">
            <h4 Align="center">INGREDIENTES</h4>
            <p Align="justify">PARA 8 PERSONAS</p>
            175 g de mantequilla,
            a temperatura ambiente, y un poco más para
            engrasar
            150 g de harina de fuerza
            2 cucharadas de cacao
            en polvo sin azúcar
            1 cucharadita de levadura en polvo
            175 g de azúcar de granulado fino
            3 huevos</p>
            <p Align="justify">PARA ADORNAR</p>
            azúcar de lustre para espolvorear,
            virutas de chocolate</p>
            <p Align="justify">RELLENO</p>
            450 g de ricota
            100 g de chocolate negro
            con poco azúcar, rallado 115 g de azúcar de granulado fino
            3 cucharadas de marsala
            50 g de piel de cítricos
            confitada, picada
            25 g de almendras, picadas</p>
        </div>

        <div class="col-md-4">
            <h4 Align="center">PREPARACIÓN</h4>
            <p Align="justify">1 Precaliente el horno a 190 °C. Engrase y forre la base de un molde redondo para pasteles de 18 cm. Tamice la harina, el cacao y la levadura en un cuenco grande. Añada la mantequilla, el azúcar y los huevos y bátalo hasta obtener una pasta suave y cremosa. Viértala en el molde y cueza el bizcocho 30 o 40 minutos o hasta que haya subido y esté consistente al tacto. Déjelo 5 minutos en el molde y después sáquelo y déjelo en una rejilla metálica para que termine de enfriarse.</p>
            <p Align="justify">2 Limpie y seque el molde y vuelva a engrasarlo y forrarlo. Para hacer el relleno, pase la ricota por un colador, sobre un cuenco. Añada el chocolate rallado, el azúcar y el marsala y bátalo bien, hasta que la pasta esté ligera y esponjosa. Agregue la piel de cítricos y la almendra.</p>
            <p Align="justify">3 Recorte una capa fina de la corteza del bizcocho y deséchela. Rebánelo en 3 capas. Ponga la primera en el molde preparado y cúbrala con la mitad de la pasta de queso. Ponga otra capa de bizcocho y de ricota y coloque el último trozo de bizcocho. Presione un poco, ponga encima un plato y un peso y déjelo enfriar en el frigorífico 8 horas o toda la noche. Para servirlo, vuelque el pastel en una fuente. Espolvoréelo con azúcar de lustre y adórnelo con las virutas de chocolate.</p>
        </div>

