            <p Align="justify">RELLENO</p>
            <p Align="justify">300 ml de nata extragrasa
                25 g de azúcar de lustre,
                tamizado
                50 g de almendras tostadas,
                picadas
                225 g de cerezas negras, deshuesadas
                50 g de chocolate negro con poco azúcar, picado</p>
            <p Align="justify">PARA ADORNAR</p>
            <p Align="justify">1 cucharada de cacao
                en polvo sin azúcar 1 cucharada de azúcar de lustre
