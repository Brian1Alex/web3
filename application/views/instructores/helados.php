            <p Align="justify">1._ Trocee el chocolate blanco y resérvelo. Ponga la maicena, la esencia de vainilla y las yemas de huevo en un cuenco resistente al calor y bátalo bien. Vierta la leche en un cazo grande de base gruesa y déjela a fuego lento hasta que hierva. Vaya vertiéndola poco a poco sobre la mezcla de huevo, removiendo.</p>
            <p Align="justify">2._ Cuélelo en el mismo cazo y déjelo a fuego lento, removiendo, hasta que se espese. Retírelo del fuego, añada el chocolate blanco y remueva hasta que se derrita. Incorpore la nata. Reserve 150 ml de la crema y vierta el resto en un recipiente grande para el congelador. Tápelo y congélelo 2 horas o hasta que empiece a cuajar. Derrita el chocolate negro a la naranja y añádalo a los 150 ml de crema reservados.</p>
            <p Align="justify">3._ Saque el helado parcialmente congelado del congelador y bátalo con un tenedor. Añada la crema de chocolate negro a cucharadas y remueva con un tenedor para obtener el aspecto de vetas de mármol. Déjelo 8 horas o toda la noche en el congelador, hasta que esté duro. Déjelo 30 minutos en el frigorífico antes de servirlo. Haga bolas de helado y repártalas en copas individuales. Adórnelas con la ralladura de naranja. Sirva el helado con unos gajos de naranja.</p>
