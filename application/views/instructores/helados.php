        <div class="col-md-4">
            <h4 Align="center">INGREDIENTES</h4>
            <p Align="justify">PARA 6 PERSONAS</p>
            <p Align="justify">HELADO</p>
            <p Align="justify">1 huevo</p>
            <p Align="justify">3 yemas de huevo</p>
            <p Align="justify">85 g de azúcar de granulado fino</p>
            <p Align="justify">300 ml de leche entera</p>
            <p Align="justify">250 g de chocolate negro con poco azúcar</p>
            <p Align="justify">300 ml de nata extragrasa</p>
            <p Align="justify">CESTITAS</p>
            <p Align="justify">100 g de chocolate negro con poco azúcar</p>
        </div>

        <div class="col-md-4">
            <h4 Align="center">PREPARACIÓN</h4>
            <p Align="justify">1._ En un bol, bata bien el huevo con las yemas y el azúcar. Caliente la leche, sin dejar que llegue a hervir. Vierta la leche poco a poco sobre el huevo, batiendo bien. Encaje el cuenco en un cazo con agua hirviendo a fuego lento y caliente la crema, removiendo, hasta que se espese lo suficiente como para cubrir el dorso de una cuchara de madera.</p>
            <p Align="justify">2._ Trocee el chocolate y añádalo a la crema. Remueva hasta que se haya fundido. Cubra la crema con una hoja de papel vegetal humedecido y déjela enfriar.</p>
            <p Align="justify">3._ Bata la nata hasta que quede cremosa e incorpórela a la crema. Viértala en un recipiente adecuado y congélela 1 o 2 horas, hasta que se haya helado 2,5 cm desde los bordes. Pase el helado a un cuenco enfriado y bátalo hasta que la crema quede suave. Déjela en el congelador hasta que se endurezca.</p>
            <p Align="justify">4._ Para preparar las cestitas, ponga boca abajo un molde múltiple para magdalenas y forre 6 moldes alternos con film transparente. Vierta el chocolate derretido en un cucurucho de papel y corte la punta.</p>
            <p Align="justify">5._ Dibuje con chocolate un círculo alrededor de la base de cada molde, y después una rejilla; aplique con cuidado otra capa. Déjelo cuajar en el frigorífico y después retire el film. Sirva el helado en las cestitas.</p>
