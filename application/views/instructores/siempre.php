        <div class="col-md-4">
            <h4 Align="center">INGREDIENTES</h4>
            <p Align="justify">PARA 6 PERSONAS</p>
            PASTA</p>
            <p Align="justify">150 g de harina</p>
            <p Align="justify">2 cucharaditas de cacao en polvo</p>
            <p Align="justify">2 cucharaditas de azúcar de lustre</p>
            <p Align="justify">1 pizca de sal</p>
            <p Align="justify">50 g de mantequilla fría, en trocitos</p>
            <p Align="justify">1 yema de huevo</p>
            <p Align="justify">agua muy fría</p>
            <p Align="justify">RELLEBO DE CHOCOLATE</p>
            <p Align="justify">200 g de chocolate negro con un 70% de cacao</p>
            <p Align="justify">25 g de mantequilla sin sal,a temperatura ambiente 250 ml de nata extragrasa 1 cucharadita de ron negro (opcional)</p>
            <p Align="justify">virutas de chocolate, para adornar</p>

        </div>
        <div class="col-md-4">
            <h4 Align="center">PREPARACIÓN</h4>
            <p Align="justify">1._ Unte de mantequilla un molde acanalado desmontable de 22 cm de diámetro. Tamice en el recipiente del robot de cocina la harina, el cacao, el azúcar de lustre y la sal, incorpore la mantequilla y amáselo hasta obtener una consistencia como de pan rallado. Pásela a un cuenco y trabájela con la yema y un poco de agua muy fría para obtener una pasta homogénea. En una superficie enharinada, extiéndala con el rodillo en un círculo 8 cm más grande que el molde. Coloque con cuidado la pasta en el molde, adaptándola. Pase el rodillo por el borde del molde para recortar el exceso de pasta. Ponga un redondel de papel vegetal sobre la masa, esparza unas legumbres secas y déjela 30 minutos en el frigorífico. Mientras tanto, precaliente el horno a 190 °C.</p>
            <p Align="justify">2._ Saque el molde del frigorífico y cueza la base de la tarta 15 minutos; retire las legumbres y el papel vegetal y hornéela 5 minutos más.</p>
            <p Align="justify">3._ Para preparar el relleno, trocee el chocolate y póngalo en un cuenco con la mantequilla. Lleve la nata a ebullición y viértala encima. Remuévalo bien, añada el ron si lo desea y siga removiendo hasta que el chocolate se funda por completo. Vierta el relleno en la base y deje la tarta 3 horas en el frigorífico.</p>
